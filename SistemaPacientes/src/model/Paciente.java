package model;

/**
 *
 * @author sev
 */
public class Paciente {
    private int codigo;
    private String nome;
    private double peso;
    private int idade;
    private String sexo;
    private double altura;

    public Paciente(int codigo, String nome, double peso, int idade, String sexo, double altura) {
        this.codigo = codigo;
        this.nome = nome;
        this.peso = peso;
        this.idade = idade;
        this.sexo = sexo;
        this.altura = altura;
    }
    
    

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public double getPeso() {
        return peso;
    }

    public void setPeso(double peso) {
        this.peso = peso;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    public double getAltura() {
        return altura;
    }

    public void setAltura(double altura) {
        this.altura = altura;
    }
    
    
}
